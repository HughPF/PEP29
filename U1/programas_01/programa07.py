# Escribe un programa que use la función print() para:
# - Escribir el número 34 en binario, octal y hexadecimal.
# - Escribir los números 0b1001, 0o12, 0xa3f en decimal.

# Número 34 en distintas bases
n = 34
print("Número 34 en binario:", bin(n))
print("Número 34 en octal:", oct(n))
print("Número 34 en hexadecimal:", hex(n))

print()  # Separador visual

# Números en distintas bases escritos en decimal
print("0b1001 en decimal:", int(0b1001))
print("0o12 en decimal:", int(0o12))
print("0xa3f en decimal:", int(0xa3f))
