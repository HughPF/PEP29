# Enunciado: Escribe un programa que pregunte al usuario dos números
# y calcule su suma, resta, multiplicación, división, módulo y potencia.

x = float(input("Introduce el primer número: "))
y = float(input("Introduce el segundo número: "))

print("Suma:", x + y)
print("Resta:", x - y)
print("Multiplicación:", x * y)
print("División:", x / y)
print("Módulo:", x % y)
print("Potencia:", x ** y)
